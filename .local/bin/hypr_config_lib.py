#!/usr/bin/env python3
"""Shared logic for generating Hyprland's keybinds.lua / appearance.lua from
the JSON data files that the GUI editor (hypr-config-editor.py) manages.

These generated .lua files are require()'d from hyprland.lua. They are
regenerated in full on every save, so keybinds.json / appearance.json are
the single source of truth - never hand-edit the .lua output.
"""

import json
import subprocess
from pathlib import Path

HYPR_DIR = Path.home() / ".config" / "hypr"
KEYBINDS_JSON = HYPR_DIR / "keybinds.json"
APPEARANCE_JSON = HYPR_DIR / "appearance.json"
MONITORS_JSON = HYPR_DIR / "monitors.json"
KEYBINDS_LUA = HYPR_DIR / "keybinds.lua"
APPEARANCE_LUA = HYPR_DIR / "appearance.lua"
MONITORS_LUA = HYPR_DIR / "monitors.lua"
KEYBINDS_TXT = HYPR_DIR / "keybinds.txt"

GENERATED_HEADER = (
    "-- AUTO-GENERATED by hypr-config-editor.py - do not hand-edit.\n"
    "-- Edit via SUPER+SHIFT+E, or edit the .json source and re-run the generator.\n\n"
)


def load_keybinds():
    with open(KEYBINDS_JSON, encoding="utf-8") as f:
        return json.load(f)


def save_keybinds(rows):
    with open(KEYBINDS_JSON, "w", encoding="utf-8") as f:
        json.dump(rows, f, indent=2)
        f.write("\n")


def load_appearance():
    with open(APPEARANCE_JSON, encoding="utf-8") as f:
        return json.load(f)


def save_appearance(data):
    with open(APPEARANCE_JSON, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)
        f.write("\n")


def load_monitors():
    if not MONITORS_JSON.exists():
        return [{"output": "", "mode": "preferred", "position": "auto", "scale": "auto"}]
    with open(MONITORS_JSON, encoding="utf-8") as f:
        return json.load(f)


def save_monitors(rows):
    with open(MONITORS_JSON, "w", encoding="utf-8") as f:
        json.dump(rows, f, indent=2)
        f.write("\n")


def _lua_str(s):
    """Quote a Lua string literal safely."""
    return '"' + s.replace("\\", "\\\\").replace('"', '\\"') + '"'


def generate_keybinds_lua():
    rows = load_keybinds()
    lines = [GENERATED_HEADER]
    for row in rows:
        combo = _lua_str(row["combo"])
        dispatcher = row["dispatcher"]
        opts = row.get("opts", "").strip()
        desc = row.get("desc", "").strip()
        call = f"hl.bind({combo}, {dispatcher}"
        if opts:
            call += f", {opts}"
        call += ")"
        if desc:
            call += f"  -- {desc}"
        lines.append(call)
    KEYBINDS_LUA.write_text("\n".join(lines) + "\n", encoding="utf-8")


def generate_appearance_lua():
    a = load_appearance()
    blur_enabled = "true" if a["blur_enabled"] else "false"
    shadow_enabled = "true" if a["shadow_enabled"] else "false"
    lua = GENERATED_HEADER + f"""hl.config({{
    general = {{
        gaps_in  = {a['gaps_in']},
        gaps_out = {a['gaps_out']},

        border_size = {a['border_size']},

        col = {{
            active_border   = {{ colors = {{"rgba({a['active_color_1']})", "rgba({a['active_color_2']})"}}, angle = {a['gradient_angle']} }},
            inactive_border = "rgba({a['inactive_color']})",
        }},

        resize_on_border = true,
        allow_tearing    = false,
        layout           = "dwindle",
    }},

    decoration = {{
        rounding       = {a['rounding']},
        rounding_power = 2,

        active_opacity   = {a['active_opacity']},
        inactive_opacity = {a['inactive_opacity']},

        shadow = {{
            enabled      = {shadow_enabled},
            range        = 12,
            render_power = 3,
            color        = 0x66000000,
        }},

        blur = {{
            enabled  = {blur_enabled},
            size     = {a['blur_size']},
            passes   = {a['blur_passes']},
            vibrancy = 0.1696,
        }},
    }},

    animations = {{
        enabled = true,
    }},
}})

hl.curve("easeOutQuint", {{ type = "bezier", points = {{ {{0.23, 1}}, {{0.32, 1}} }} }})
hl.curve("linear",       {{ type = "bezier", points = {{ {{0, 0}},    {{1, 1}}   }} }})

hl.animation({{ leaf = "global",     enabled = true, speed = 6,  bezier = "default" }})
hl.animation({{ leaf = "border",     enabled = true, speed = 8,  bezier = "default" }})
hl.animation({{ leaf = "windows",    enabled = true, speed = 4,  bezier = "easeOutQuint" }})
hl.animation({{ leaf = "windowsOut", enabled = true, speed = 4,  bezier = "easeOutQuint", style = "popin 80%" }})
hl.animation({{ leaf = "fade",       enabled = true, speed = 4,  bezier = "default" }})
hl.animation({{ leaf = "workspaces", enabled = true, speed = 5,  bezier = "easeOutQuint", style = "slide" }})
"""
    APPEARANCE_LUA.write_text(lua, encoding="utf-8")


def _lua_scale(raw):
    """'auto' stays a quoted string; anything else must be a bare Lua number."""
    raw = str(raw).strip()
    if raw.lower() == "auto":
        return '"auto"'
    try:
        return str(float(raw))
    except ValueError:
        raise ValueError(f"scale must be 'auto' or a number, got {raw!r}")


def generate_monitors_lua():
    rows = load_monitors()
    lines = [GENERATED_HEADER]
    for row in rows:
        lines.append(f"""hl.monitor({{
    output   = {_lua_str(row['output'])},
    mode     = {_lua_str(row['mode'])},
    position = {_lua_str(row['position'])},
    scale    = {_lua_scale(row['scale'])},
}})
""")
    MONITORS_LUA.write_text("\n".join(lines) + "\n", encoding="utf-8")


def generate_cheatsheet():
    rows = load_keybinds()
    width = max(len(r["combo"]) for r in rows) + 2
    lines = [f"{r['combo']:<{width}}→  {r.get('desc', '')}" for r in rows]
    KEYBINDS_TXT.write_text("\n".join(lines) + "\n", encoding="utf-8")


def generate_all():
    generate_keybinds_lua()
    generate_appearance_lua()
    generate_monitors_lua()
    generate_cheatsheet()


if __name__ == "__main__":
    # Lets install.sh regenerate the .lua/.txt files from the .json sources
    # on a freshly-cloned machine without opening the GUI editor.
    generate_all()


def reload_hyprland():
    return subprocess.run(
        ["hyprctl", "reload"], capture_output=True, text=True, timeout=10
    )


def get_available_modes():
    """Map each currently connected output name to its supported
    'WIDTHxHEIGHT@RATE' modes (as accepted by the 'mode' config field),
    for populating a resolution dropdown. Empty dict if hyprctl is
    unreachable (e.g. editing config outside a running session)."""
    result = subprocess.run(
        ["hyprctl", "-j", "monitors"], capture_output=True, text=True, timeout=10
    )
    if result.returncode != 0:
        return {}
    modes = {}
    for m in json.loads(result.stdout):
        modes[m["name"]] = [mode.removesuffix("Hz") for mode in m.get("availableModes", [])]
    return modes


def detect_monitors():
    """Query the live compositor for currently connected outputs, so the
    Monitors tab can be seeded with real names/modes instead of guesses."""
    result = subprocess.run(
        ["hyprctl", "-j", "monitors"], capture_output=True, text=True, timeout=10
    )
    if result.returncode != 0:
        raise RuntimeError(result.stderr or "hyprctl monitors failed")
    rows = []
    for m in json.loads(result.stdout):
        rows.append({
            "output": m["name"],
            "mode": f"{m['width']}x{m['height']}@{m['refreshRate']:.2f}",
            "position": f"{m['x']}x{m['y']}",
            "scale": f"{m['scale']:.6g}",
        })
    return rows
